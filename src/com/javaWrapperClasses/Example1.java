package com.javaWrapperClasses;

public class Example1 
{
	

	public static void main(String[] args) 
	{
		
		int value = 30;
		
		System.out.println(value);
		
		Integer integerValue = -2147483648;
		
		System.out.println(integerValue);
		
		System.out.println("---------------------------------------");
		
		System.out.println(integerValue.SIZE);
		System.out.println(integerValue.BYTES);
		System.out.println(integerValue.MAX_VALUE);
		System.out.println(integerValue.MIN_VALUE);

	}

}
